/*
 * Esta clase contiene las variables que se usan para calcular las pólizas
 */
package tablasdereferencia;

/**
 * Tablas de referencia para el calculo de las pólizas
 * @author Felipe Cadavid y Jeison Velasquez
 * @since 08-10-2017
 */
public class DatosOperaciones {
    
    private double mx;
    private int edad;
    private double dx;
    private double ix;
    private double cx;

    public DatosOperaciones() {
    }

    public double getMx() {
        return mx;
    }

    public void setMx(double mx) {
        this.mx = mx;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public double getDx() {
        return dx;
    }

    public void setDx(double dx) {
        this.dx = dx;
    }

    public double getIx() {
        return ix;
    }

    public void setIx(double ix) {
        this.ix = ix;
    }

    public double getCx() {
        return cx;
    }

    public void setCx(double cx) {
        this.cx = cx;
    }

    
    
    
}
